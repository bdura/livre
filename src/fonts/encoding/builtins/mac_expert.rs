use crate::fonts::encoding::{CharacterSet, Decode, Glyph};

/// Encoding for "expert fonts".
///
/// > An encoding for use with expert fonts â€” ones containing the expert character set.
/// > PDF processors shall have a predefined encoding named MacExpertEncoding.
/// > Despite its name, it is not a platform-specific encoding; however, only certain
/// > fonts have the appropriate character set for use with this encoding.
/// > No such fonts are among the standard 14 predefined fonts.
#[derive(Debug)]
pub struct MacExpertEncoding;

impl Decode for MacExpertEncoding {
    fn decode(&self, code: u8) -> u16 {
        MAC_EXPERT_ENCODING[code as usize].expect("character should be in the character set.")
    }

    fn character_set(self) -> Vec<Option<u16>> {
        MAC_EXPERT_ENCODING.to_vec()
    }
}

